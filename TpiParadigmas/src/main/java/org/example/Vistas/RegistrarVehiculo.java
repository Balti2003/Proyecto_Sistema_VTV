    private void botonCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCancelarActionPerformed
        // TODO add your handling code here:
        if(volverCallBack!=null){
            volverCallBack.run();
            setCamposVacios();
            this.dispose();
        }
    }//GEN-LAST:event_botonCancelarActionPerformed

    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        // TODO add your handling code here:
        cliente = gestorCliente.buscarClientePorDni(campoIngresoDni.getText());
        
        if (cliente !=null){
            campoMuestraCliente.setText(cliente.getNombre() + " " + cliente.getApellido());
            this.panelDatosVehiculo.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(
                    null,
                    "No se pudo encontrar el cliente, revise el numero de documento e intente nuevamente",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnBuscarClienteActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        gestorVehiculo.registrarVehiculo(this.cliente,
                campoMarca.getText(),
                campoModelo.getText(), 
                (TipoVehiculo) selectorTipoVehiculo.getSelectedItem(),
                campoAnoFabricacion.getText(),
                campoPatente.getText()
        );
        
        JOptionPane.showMessageDialog(
                    null,
                    "Vehiculo Registrado Correctamente!",
                    "Vehiculo Registrado",
                    JOptionPane.INFORMATION_MESSAGE
            );
        
        if(volverCallBack!=null){
            volverCallBack.run();
            setCamposVacios();
            this.dispose();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed


    
    public void mostrarPantalla(){
        setListadoTiposVehiculo();
        this.panelDatosVehiculo.setVisible(false);
        setCamposVacios();
        this.setVisible(true);
    }
    
    
    public void setOnVolverCallback(Runnable callback) {
        this.volverCallBack = callback;
    }
    
    private DefaultComboBoxModel setListadoTiposVehiculo(){
        listadoTiposVehiculo.removeAllElements();
        
        for(TipoVehiculo tipoVehiculo : gestorVehiculo.getListaTipoVehiculos()){
            listadoTiposVehiculo.addElement(tipoVehiculo);
        }
        selectorTipoVehiculo.setModel(listadoTiposVehiculo);
        
        return listadoTiposVehiculo;
    }
    
    private void setCamposVacios(){
        campoIngresoDni.setText("");
        campoPatente.setText("");
        campoMarca.setText("");
        campoModelo.setText("");
        campoAnoFabricacion.setText("");
        campoMuestraCliente.setText("");   
    }

